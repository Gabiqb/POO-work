package lab4;

public class Sofer {
    public String nume;

    public String prenume;
    public int varsta;
    public int nr_permis;

    public String getNume() {
        return nume;
    }

    public void setNume(String nume) {
        this.nume = nume;
    }

    public String getPrenume() {
        return prenume;
    }

    public void setPrenume(String prenume) {
        this.prenume = prenume;
    }

    public int getVarsta() {
        return varsta;
    }

    public void setVarsta(int varsta) {
        this.varsta = varsta;
    }

    public int getNr_permis() {
        return nr_permis;
    }

    public void setNr_permis(int nr_permis) {
        this.nr_permis = nr_permis;
    }
}
